export const getMovies = async (page = 1) => {
  const response = await fetch(
    `http://localhost:8080/api/movies/tmdb/movies?page=${page}`,
     {
    headers: {
      'Authorization': window.localStorage.getItem('token')
    }
  }
  )
  return response.json();
};

